package chap5.task4;
public class formula {
    //method tukar Celsius ke Fahrenheit
    public static double CelsiusToFahrenheit(double celsius) {
        return (celsius * 9 / 5) + 32; //formula tukar suhu
    }
    
    //method tukar Kilometer ke Feet
    public static double KilometerToFeet(double kilometers) {
        return kilometers * 3280.84; //formula tukar jarak
    }
    
    //method tukar Kilogram ke Pound
    public static double KilogramToPounds(double kilograms) {
        return kilograms * 2.20462; // Formula penukaran berat
    }}
