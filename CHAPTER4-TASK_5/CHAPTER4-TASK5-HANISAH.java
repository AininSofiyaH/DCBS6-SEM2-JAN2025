package chap4task5;

public class Chap4task5 {

    public static void main(String[] args) {
        System.out.println("Displaying the sequence: 99 88 77 66 55 44 33 22 11\n");
        
        for (int i = 99; i >= 11; i -= 11) {
            System.out.print(i + " ");
        }
        
        System.out.println();
    }
    
}
